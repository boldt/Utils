package de.dennis_boldt.utils;

public class MimeTypeUtilException extends Exception {

	private static final long serialVersionUID = 1L;

	public MimeTypeUtilException() {
		super();
	}

	public MimeTypeUtilException(String message) {
		super(message);
	}

	public MimeTypeUtilException(String message, Throwable cause) {
		super(message, cause);
	}

	public MimeTypeUtilException(Throwable cause) {
		super(cause);
	}

}
